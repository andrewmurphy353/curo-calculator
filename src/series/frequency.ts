/**
 * Frequency demarcates the compounding period between two cash flows. Options are:
 *
 * * Weekly
 * * Fortnightly
 * * Monthly
 * * Quarterly
 * * HalfYearly
 * * Yearly
 */
export enum Frequency {
  Weekly = 'Weekly',
  Fortnightly = 'Fortnightly',
  Monthly = 'Monthly',
  Quarterly = 'Quarterly',
  HalfYearly = 'HalfYearly',
  Yearly = 'Yearly'
}
